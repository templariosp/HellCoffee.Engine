    public static Vector2 Measure(string text, int scale = 1)
        => new(text.Length * GlyphW * scale, GlyphH * scale);

    public static void Draw(SpriteBatch sb, Texture2D pixel, string text,
        Vector2 position, Color color, int scale = 1)
    {
        float x = position.X;
        foreach (char raw in text)
        {
            char c = char.ToUpper(raw);
            if (!Glyphs.TryGetValue(c, out var glyph))
            {
                x += GlyphW * scale;
                continue;
            }
            for (int row = 0; row < 7; row++)
            {
                byte line = glyph[row];
                for (int col = 0; col < 5; col++)
                {
                    if ((line & (0x10 >> col)) != 0)
                    {
                        sb.Draw(pixel,
                            new Rectangle((int)x + col * scale,
                                          (int)position.Y + row * scale,
                                          scale, scale),
                            color);
                    }
                }
            }
            x += GlyphW * scale;
        }
    }

    /// <summary>Desenha texto com sombra de 1px para melhor legibilidade.</summary>
    public static void DrawWithShadow(SpriteBatch sb, Texture2D pixel, string text,
        Vector2 position, Color color, int scale = 1)
    {
        Draw(sb, pixel, text, position + new Vector2(scale, scale), Color.Black * 0.8f, scale);
        Draw(sb, pixel, text, position, color, scale);
    }
}
