namespace HellCoffee.Engine.Debug;

/// <summary>
/// Fonte bitmap 5×7 pixels embutida em código — sem dependência de SpriteFont ou arquivos externos.
/// Cada glifo é 7 bytes; cada byte representa uma linha de 5 pixels
/// onde o bit 4 é o pixel mais à esquerda.
/// </summary>
public static class PixelFont
{
    public const int GlyphW = 6; // 5px + 1px espaço
    public const int GlyphH = 8; // 7px + 1px espaço

